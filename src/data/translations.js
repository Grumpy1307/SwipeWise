// Cache for API translated cards to prevent repeated translation calls
const translationCache = {};

/**
 * Translates a game/learn card content dynamically using the Gemini API
 * @param {Object} card - The card object
 * @param {string} targetLang - Language code ('hi', 'ar', 'es')
 * @param {string} apiKey - Gemini API Key
 * @returns {Promise<Object>} The translated card object
 */
export async function translateCard(card, targetLang, apiKey) {
  if (targetLang === "en" || !targetLang) return card;
  
  const cacheKey = `${card.id}_${targetLang}`;
  if (translationCache[cacheKey]) {
    return { ...card, ...translationCache[cacheKey] };
  }

  // Fallbacks in case API fails
  const langNames = { hi: "Hindi", ar: "Arabic", es: "Spanish" };
  const targetLangName = langNames[targetLang] || "English";

  try {
    if (!apiKey) {
      throw new Error("API Key missing");
    }

    const systemPrompt = `You are an expert translator specializing in translating financial regulatory content, fraud definitions, and educational warnings from English to ${targetLangName}. 
Translate the provided card details carefully. Ensure any local regulatory terms fit the context naturally. 
Output STRICTLY a JSON object with these exact keys:
{
  "profileName": "translated profile name",
  "tag": "translated tag (like Sponsored, Ad, etc.)",
  "content": "translated post content",
  "explanation": "translated explanation",
  "redFlags": ["translated red flag 1", "translated red flag 2", ...]
}
Do not write any markdown code fences or explanatory text. Return ONLY the JSON object.`;

    const prompt = `Translate this card data:
{
  "profileName": "${card.profileName || ''}",
  "tag": "${card.tag || ''}",
  "content": "${card.content.replace(/"/g, '\\"')}",
  "explanation": "${card.explanation.replace(/"/g, '\\"')}",
  "redFlags": ${JSON.stringify(card.redFlags || [])}
}`;

    const res = await fetch(`https://generativelanguage.googleapis.com/v1/models/gemini-2.5-flash:generateContent?key=${apiKey}`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        contents: [{ parts: [{ text: `${systemPrompt}\n\n${prompt}` }] }]
      })
    });

    if (!res.ok) {
      throw new Error(`API error: ${res.status}`);
    }

    const data = await res.json();
    const responseText = data.candidates?.[0]?.content?.parts?.[0]?.text;
    if (!responseText) throw new Error("Empty response");

    const jsonMatch = responseText.match(/\{[\s\S]*\}/);
    if (!jsonMatch) throw new Error("Invalid format");

    const parsedTranslation = JSON.parse(jsonMatch[0]);
    translationCache[cacheKey] = parsedTranslation;

    return { ...card, ...parsedTranslation };
  } catch (error) {
    console.warn("Translation failed. Falling back to English.", error);
    // Simple placeholder warning addition to the card so user knows it's English
    const note = targetLang === "hi" ? " [अनुवाद उपलब्ध नहीं है]" : targetLang === "ar" ? " [الترجمة غير متوفرة]" : " [Translation unavailable]";
    return {
      ...card,
      profileName: card.profileName + note
    };
  }
}
